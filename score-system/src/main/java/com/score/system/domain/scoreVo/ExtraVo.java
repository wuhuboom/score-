package com.score.system.domain.scoreVo;

public class ExtraVo {

    private Integer length;

    private String numberofperiods;

    private String periodlength;

    public Integer getLength() {
        return length;
    }

    public void setLength(Integer length) {
        this.length = length;
    }

    public String getNumberofperiods() {
        return numberofperiods;
    }

    public void setNumberofperiods(String numberofperiods) {
        this.numberofperiods = numberofperiods;
    }

    public String getPeriodlength() {
        return periodlength;
    }

    public void setPeriodlength(String periodlength) {
        this.periodlength = periodlength;
    }
}
